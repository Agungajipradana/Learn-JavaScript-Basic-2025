// Math is a built-in non-primitive object that provides mathematical constants and functions.

console.log(Math.PI);          // Output: 3.141592653589793 (Mathematical constant Ï€)
console.log(Math.E);           // Output: 2.718281828459045 (Euler's number)

// Using Math methods
console.log(Math.round(4.7));  // Output: 5 (Rounds to nearest integer)
console.log(Math.floor(4.7));  // Output: 4 (Rounds down)
console.log(Math.ceil(4.1));   // Output: 5 (Rounds up)
console.log(Math.max(10, 20, 5));  // Output: 20 (Returns the max value)
console.log(Math.min(10, 20, 5));  // Output: 5  (Returns the min value)

// Checking the type
console.log(typeof Math);      // Output: "object"
